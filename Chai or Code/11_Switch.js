// Switch in javascript

let key = 17;
switch (key) {
  case 11:
    console.log("Number is 11");
    break;
  case 12:
    console.log("Number is 12");
    break;
  case 13:
    console.log("Number is 13");
    break;
  case 14:
    console.log("Number is 14");
    break;
  case 10:
    console.log("Number is 10");
    break;

  default:
    console.log("Number not founded");

    break;
}
