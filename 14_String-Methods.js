// String popular methods

let str = "vaibhav";
console.log(str.length); // returns the length of the string
console.log(str.toUpperCase()); // returns the string in uppercase
console.log(str.toLowerCase()); // returns the string in lowercase
console.log(str.trim()); // returns the string without spaces at the start and end
console.log(str.split("")); // returns an array of characters in the string
//console.log(str.join("")); // returns a string with all elements of the array joined by the string
console.log(str.includes("a")); // returns true if the string includes the specified value, false otherwise
console.log(str.startsWith("va")); // returns true if the string starts with the specified value, false
console.log(str.endsWith("av")); // returns true if the string ends with the specified value, false
console.log(str.repeat(3)); // returns the string repeated the specified number of times
console.log(str.replace("a", "e")); // returns the string with the specified value replaced
console.log(str.search("a")); // returns the index of the first occurrence of the specified value,
console.log(str.slice(1, 4)); // returns the characters in the string between the specified
console.log(str.substring(1, 4)); // returns the characters in the string between the specified
console.log(str.substr(1, 4)); // returns the characters in the string between the specified
console.log(str.indexOf("a")); // returns the index of the first occurrence of the specified value
console.log(str.lastIndexOf("a")); // returns the index of the last occurrence of the specified value
console.log(str.charAt(1)); // returns the character at the specified index
