// //DOM --> Document object model

// Accessing Elements:

// document.getElementById('id'): Selects an element by its ID.
// document.getElementsByClassName('class'): Selects elements by their class name.
// document.getElementsByTagName('tag'): Selects elements by their tag name.
// document.querySelector('selector'): Selects the first element that matches a CSS selector.
// document.querySelectorAll('selector'): Selects all elements that match a CSS selector.
// Traversing the DOM:

// .parentNode: Accesses the parent node of an element.
// .children: Returns a live HTMLCollection of child elements.
// .firstElementChild: Accesses the first child element.
// .lastElementChild: Accesses the last child element.
// .previousElementSibling: Accesses the previous sibling element.
// .nextElementSibling: Accesses the next sibling element.
// Creating and Manipulating Elements:

// document.createElement('tag'): Creates a new element.
// element.appendChild(child): Appends a child element to a parent element.
// element.removeChild(child): Removes a child element from a parent element.
// element.setAttribute('attribute', 'value'): Sets an attribute on an element.
// element.classList.add('class'): Adds a class to an element.
// element.classList.remove('class'): Removes a class from an element.
// element.classList.toggle('class'): Toggles a class on an element.
